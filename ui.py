def check_len_text_input(text, n):  # Функция для проверки длины текста
    while len(text) <= n:  # Пока длина текста меньше или равна n
        print(f'Текст должен быть больше {n} символов\n')  # Вывод сообщения о необходимости большего количества символов
        text = input('Введите тескт: ')  # Ввод текста
    else:
        return text  # Возврат текста

