import Notepad  # Импорт модуля Notepad

def create_note(number):  # Функция для создания заметки
    title = check_len_text_input(
        input('Введите Название заметки: '), number)  # Ввод названия заметки
    body = check_len_text_input(
        input('Введите Описание заметки: '), number)  # Ввод описания заметки
    return Notepad.Note(title=title, body=body)  # Создание заметки и возврат её

